function makeHinglishCopy(value: string) {
  return value
    .replace(/\bSave\b/g, "Save karo")
    .replace(/\bsave\b/g, "save karo")
    .replace(/\bCheck\b/g, "Check karo")
    .replace(/\bcheck\b/g, "check karo")
    .replace(/\bOpen\b/g, "Open karo")
    .replace(/\bopen\b/g, "open karo")
    .replace(/\bAsk\b/g, "Poochho")
    .replace(/\bask\b/g, "poochho")
    .replace(/\bBuy\b/g, "Buy karo")
    .replace(/\bbuy\b/g, "buy karo")
    .replace(/\bYour\b/g, "Aapka")
    .replace(/\byour\b/g, "aapka")
    .replace(/\bBefore\b/g, "Pehle")
    .replace(/\bbefore\b/g, "pehle");
}

const COPY_BY_LANGUAGE: Record<LanguageCode, Record<CopyKey, string>> = {
  english: ENGLISH_COPY,
  hindi: HINDI_COPY,
  hinglish: HINGLISH_COPY
};

export const LANGUAGE_OPTIONS: Array<{ code: LanguageCode; label: string; shortLabel: string }> = [
  { code: "english", label: "English", shortLabel: "En" },
  { code: "hindi", label: "हिन्दी", shortLabel: "हि" },
  { code: "hinglish", label: "Hinglish", shortLabel: "Hi-En" }
];

export function t(language: LanguageCode, key: CopyKey): string {
  return COPY_BY_LANGUAGE[language]?.[key] ?? ENGLISH_COPY[key];
}

export function languageLabel(language: LanguageCode): string {
  return LANGUAGE_OPTIONS.find((item) => item.code === language)?.label ?? "English";
}

export function simpleTrustMeaning(status: string, canRecommend: boolean, language: LanguageCode): string {
  if (!canRecommend) {
    if (status === "limited_evidence") return t(language, "notEnoughProof");
    if (status === "data_degraded") return t(language, "freshProofNotReady");
    if (status === "seller_restricted") return t(language, "sellerCheckFailed");
    return t(language, "recommendationPaused");
  }
  if (status === "specific_caution") return t(language, "checkOnce");
  if (status === "conflicting_evidence") return t(language, "proofMismatchCompare");
  return t(language, "safeToCompare");
}
